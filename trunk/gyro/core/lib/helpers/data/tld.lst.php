/**
 * This fucntion is used to sort the tld array. If you have any changes, 
 * call this function with usort, and store the result in the above function
 */
function _url_sort_tld($a, $b) {
	$la = String::length($a);
	$lb = String::length($b);
	if ($la == $lb) {
		return 0;
	}
	return ($la > $lb) ? -1 : 1;
}
